job_ids = set(jid for jid, _ in HINTS.keys())
updated = 0
missing = []

for jid in sorted(job_ids):
    row = conn.execute("SELECT config_json FROM jobs WHERE id=?", (jid,)).fetchone()
    if not row:
        print(f"WARNING: job id={jid} not found in DB")
        continue
    cfg = json.loads(row[0])
    changed = False
    for rule in cfg.get("rules", []):
        key = (jid, rule["name"])
        if key in HINTS:
            rule["evidence_hint"] = HINTS[key]
            changed = True
        else:
            missing.append(key)
    if changed:
        conn.execute(
            "UPDATE jobs SET config_json=?, updated_at=? WHERE id=?",
            (json.dumps(cfg, ensure_ascii=False), NOW, jid),
        )
        updated += 1

conn.commit()
conn.close()

