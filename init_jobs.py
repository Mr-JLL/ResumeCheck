PROMPT_TEMPLATE = """# 岗位：{job_name}

## 核心职责
{key_responsibilities}

## 全局证据原则
评判时所有条件均须在简历原文中找到对应证据，禁止主观推断：
- **软性条件**：须有具体项目/数字/时间段支撑；"熟悉/了解/参与"等词汇不构成证据
- **行业条件**：须直接在该行业企业任职；供应链上下游/一般机械制造不满足
- **英语条件**：须有实际使用场景（客户沟通/外文资料/跨国项目）；仅写证书等级不满足
- **管理条件**：须有直接下属人数；"协调各方""带领项目组"不算管理经验
- **工具/软件**：须有实际应用项目描述；"了解""会用"不满足
"""


def render_prompt(job_def):
    base = PROMPT_TEMPLATE.format(
        job_name=job_def["name"],
        key_responsibilities=job_def.get("key_responsibilities", ""),
    )
    extra = job_def.get("extra_context", "").strip()
    if extra:
        base = base.replace("\n\n## 全局证据原则", f"\n\n{extra}\n\n## 全局证据原则")
    return base


